public class Arithmatic{
    public static void main(String[] args) {
        int num1 = 15, num2 = 5;
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1/num2);
        System.out.println(num1%num2); 
    }
}